import 'package:flutter/material.dart';

import 'settings_service.dart';

/// A class that many Widgets can interact with to read user settings, update
/// user settings, or listen to user settings changes.
///
/// Controllers glue Data Services to Flutter Widgets. The SettingsController
/// uses the SettingsService to store and retrieve user settings.
class SettingsController with ChangeNotifier {
  SettingsController(this._settingsService);

  // Make SettingsService a private variable so it is not used directly.
  final SettingsService _settingsService;

  // Make ThemeMode a private variable so it is not updated directly without
  // also persisting the changes with the SettingsService.
  late ThemeMode _themeMode;

  // Allow Widgets to read the user's preferred ThemeMode.
  ThemeMode get themeMode => _themeMode;

  // Default Font Size
  double _fontSize = 27;

  // Getter of font size
  double get fontSize => _fontSize;

  // Getter of saved news
  late String _savedNews;
  String get savedNews => _savedNews;

  /// Load the user's settings from the SettingsService. It may load from a
  /// local database or the internet. The controller only knows it can load the
  /// settings from the service.
  Future<void> loadSettings() async {
    // Initializing shared preferences that will load user settings
    await _settingsService.initPrefs();
    // Getting system theme mode
    _themeMode = await _settingsService.themeMode();
    // Getting system font size
    _fontSize = await _settingsService.getFontSize();
    // Getting saved news
    _savedNews = await _settingsService.getSavedNews();

    // Important! Inform listeners a change has occurred.
    notifyListeners();
  }

  /// Update and persist the ThemeMode based on the user's selection.
  Future<void> updateThemeMode(ThemeMode? newThemeMode) async {
    if (newThemeMode == null) return;

    // Do not perform any work if new and old ThemeMode are identical
    if (newThemeMode == _themeMode) return;

    // Otherwise, store the new ThemeMode in memory
    _themeMode = newThemeMode;

    // Important! Inform listeners a change has occurred.
    notifyListeners();

    // Persist the changes to a local database or the internet using the
    // SettingService.
    await _settingsService.updateThemeMode(newThemeMode);
  }

  // Calling service to update font size then notify other screens of changes
  Future<void> updateFontSize(double fontSize) async {
    await _settingsService.updateFontSize(fontSize);
    // Updating font size
    _fontSize = fontSize;
    notifyListeners();
  }

  // Retrieving saved news
  Future<void> getSavedNews() async {
    _savedNews = await _settingsService.getSavedNews();
    notifyListeners();
  }

  // Saving Saved News
  Future<void> saveSavedNews(String newsList) async {
    await _settingsService.saveSavedNews(newsList);
    getSavedNews();
  }
}
